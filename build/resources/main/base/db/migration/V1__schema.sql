create table if not exists transaction
(
    cash_entry varchar(40) not null,
    id varchar(40) not null,
     account varchar(255),
     acquirer varchar(255),
     cpty_code varchar(255),
     cpty_osl_uuid varchar(255),
     currency varchar(255),
     custodian varchar(255),
     execution_timestamp timestamp not null default '1970-01-01 00:00:01',
     fund varchar(255),
     gross_amount decimal(19,2),
     input_timestamp timestamp not null default '1970-01-01 00:00:01',
     inserted boolean not null,
     instrument varchar(255),
     net_amount decimal(19,2),
     portfolio varchar(255),
     price decimal(19,2),
     quantity decimal(19,2),
     settle_ccy varchar(255),
     settle_day varchar(255),
     side varchar(255),
     strategy varchar(255),
     trade_day varchar(255),
     trade_type varchar(255),
     transaction_type varchar(255),
     update_timestamp timestamp not null default '1970-01-01 00:00:01',
     primary key (cash_entry, id)
     );
